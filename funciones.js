// Forma 1 de sintaxis para definir (FUNCION COMUN)

function sumaTres(x){
    return (x + 3)
}

// Forma 2 de sintaxis para definir (FUNCION EN VARIABLE)
 
let sumaTres = function (x) {
    return x + 3;
}

// Forma 3 de sintaxis para definir (FUNCION DE FLECHA)

var sumaTres = (x) => {
    return x + 3;
}